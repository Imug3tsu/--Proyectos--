
package Persona1;

public class Fruta {
    private String fruta;
    private String color;

    public Fruta() {
    }

    public Fruta(String fruta, String color) {
        this.fruta = fruta;
        this.color = color;
    }

    public String getFruta() {
        return fruta;
    }

    public String getColor() {
        return color;
    }
    
}
